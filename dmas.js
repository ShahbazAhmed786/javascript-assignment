function printResult(operation, result) {
    console.log("".concat(operation, " = ").concat(result));
}
var additionResult = 5 + 3;
printResult("5 + 3", additionResult);
var subtractionResult = 10 - 2;
printResult("10 - 2", subtractionResult);
var multiplicationResult = 4 * 2;
printResult("4 * 2", multiplicationResult);
var divisionResult = 16 / 2;
printResult("16 / 2", divisionResult);
